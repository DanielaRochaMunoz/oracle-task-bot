    JOIN TODOUSER.TODOITEM t ON t.ID = s.MAIN_TASK_ID
    WHERE s.ASSIGNED_DEVELOPER_ID = v_dev
      AND t.IS_ACTIVE = 1;

    UPDATE TODOUSER.DEVELOPER_STATS
    SET 
      TOTAL_ASSIGNED_COUNT  = v_ass,
      TOTAL_COMPLETED_COUNT = v_com,
      SUM_ESTIMATED_HOURS   = v_est,
      SUM_ACTUAL_HOURS      = v_act,
      LAST_UPDATED_TS       = CURRENT_TIMESTAMP
    WHERE DEVELOPER_ID = v_dev;
  END LOOP;
  dev_stats_pkg.devs.DELETE;
END;
