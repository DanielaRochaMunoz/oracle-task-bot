DECLARE
  v_dev   NUMBER;
  v_ass   NUMBER;
  v_com   NUMBER;
  v_est   NUMBER(10,2);
  v_act   NUMBER(10,2);
BEGIN
  FOR i IN 1..dev_stats_pkg.devs.COUNT LOOP
    v_dev := dev_stats_pkg.devs(i);

